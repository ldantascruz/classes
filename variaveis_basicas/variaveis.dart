void main() {
  int idade = 22; // variável do tipo inteiro
  String nome = "Lucas"; // variável do tipo string
  double distancia = 24.89; // variável do tipo double

  print(idade);
  print(nome);
  print(distancia);

  idade = 23; // alteração da variável idade de 22 para 23
  nome = "Cruz"; // alteração da variável nome de "Lucas" para "Cruz"

  print("O ${nome} que tem ${idade} anos, correu ${distancia}km");
}
